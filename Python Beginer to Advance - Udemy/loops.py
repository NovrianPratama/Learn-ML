# LOOPING PROGRAM

range (100)
# mulai dari 0
# berakhir < 100
# increment/lompatan 1

range (1, 100)
# mulai dari 1
# berakhir < 100
# increment/lompatan 1

range (1, 100, 2)
# mulai dari 1
# berakhir < 100
# increment/lompatan 2

for n in range(0, 50, 5):
    print(n)

######## WHILE LOOP #############
print("\n")
n = 1
while n < 10:
    print(n)
    n = n + 1
    
# n = 1 : mulai dari angka
# a < 10: menentukan kondisi true dan false
## jika kondisi tidak true, maka looping tidak dilakukan
# n = n + 1 : incerement nya


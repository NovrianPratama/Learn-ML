import math

num = 10.2
num2 = 12.8
num3 = 14
num4 = 5
list1 = [3, 4, 5, 40, 12, 15]
num5 = 25
print('math ceil', math.ceil(num)) # membulatkan ke atas
print('math floor', math.floor(num2)) # membulatkan ke bawah
print('math fabs', math.fabs(num3)) # Mengembalikan nilai absolut dari x dalam tipe float.

# faktorial 5
# 5 * 4 * 3 * 2 * 1 = 120
print('math factorial', math.factorial(num4)) # membuat faktorial
print('math fsum', math.fsum(list1)) # menjumlah angka yang ada di dalam list
print('math sqrt', math.sqrt(num5)) # Menghitung akar kuadrat bilangan, misal 5x5 = 25 , berarti kuadrat 5